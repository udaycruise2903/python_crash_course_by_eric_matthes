#sorting a list 
cars=['BMW','Audi','porsche','lexus','rolls royce']
cars.sort()
print(cars)

#sorting a list in a reverse order
cars.sort(reverse=True)
print(cars)

#sort a list temporarily
cars=['BMW','Audi','porsche','lexus','rolls royce']

print('here is the sorted list')
print(sorted(cars))


print('here is the original list')
print(cars)

#reverse the order of the list
cars.reverse()
print(cars)

#length of the list
print(len(cars))



